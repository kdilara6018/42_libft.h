#include <stdio.h>
int isalnum (int c)
{
	if ((c>='0'&&c<='9')||((c>='A'&&c<='Z')||(c>='a'&&c<='z')))
	{
		return 1;
	}
	else
		return 0;
}
int main()
{
printf("%d",isalnum('*'));
}